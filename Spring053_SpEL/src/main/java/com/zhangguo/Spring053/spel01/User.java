package com.zhangguo.Spring053.spel01;

/**
 * 用户类
 */
public class User {
	/**
	 * 编号
	 */
	private int id;
	/**
	 * 姓名
	 */
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
